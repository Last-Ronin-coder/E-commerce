<script>
    $(document).ready(function() {
        $(document).on('click', '.edit-button', function(e) {
            e.preventDefault();

            var productId = $(this).data('id');

            $.ajax({
                url: '/get_product/' + productId,
                method: 'GET',
                success: function(response) {
                    $('#productTitle').val(response.title);
                    $('#productDescription').val(response.description);
                    $('#productCategory').val(response.category);
                    $('#productPrice').val(response.price);
                    $('#productQuantity').val(response.quantity);
                    $('#productImagePreview').attr('src', '/products/' + response.image);
                    $('#productForm').data('product-id', productId);

                    $('#productModal').modal('show');
                },
                error: function(xhr, status, error) {
                    console.error('Failed to fetch product details:', error);
                }
            });
        });

        $('#saveChanges').click(function() {
            var productId = $('#productForm').data('product-id');
            var formData = new FormData($('#productForm')[0]);

            $.ajax({
                url: '/edit_product/' + productId,
                method: 'POST',
                data: formData,
                contentType: false,
                processData: false,
                success: function(response) {
                    $('#productModal').modal('hide');
                    location.reload();
                },
                error: function(xhr, status, error) {
                    console.error('Failed to update product:', error);
                }
            });
        });
    });
</script>

